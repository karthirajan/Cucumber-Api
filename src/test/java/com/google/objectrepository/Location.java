package com.google.objectrepository;

public class Location {
	
	private String lat;
	
	private String lng;

	public String getLat() {
		return lat;
	}

	public void setLat(String d) {
		this.lat = d;
	}

	public String getLng() {
		return lng;
	}

	public void setLng(String lng) {
		this.lng = lng;
	}
	
	

}
